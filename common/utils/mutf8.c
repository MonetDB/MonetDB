/* Return the display width of Unicode codepoint c (-1 if c is a control
 * character, else 0, 1, or 2).  This function is similar to the
 * standard C function wcwidth, but only for Unicode codepoints */
int
charwidth(uint32_t c)
{
	if (c == 0)
		return 0;
	if ((c & ~0x80) <= 0x1F || c == 0x007F)
		return -1;				/* control character or DELETE */

	size_t min = 0;
	size_t max = (sizeof(intervals) / sizeof(intervals[0])) - 1;

	if (c >= intervals[min].first && c <= intervals[max].last) {
		while (max >= min) {
			size_t mid = (min + max) / 2;
			if (c > intervals[mid].last)
				min = mid + 1;
			else if (c < intervals[mid].first)
				max = mid - 1;
			else
				return intervals[mid].width;
		}
	}
	return 1;
}

